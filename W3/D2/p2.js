//comparison operators

console.log("comparison operators");
let  x =10, y = 3;
console.log("x==y:",x==y); //Loose equality
console.log("x===y:",x===y); //strict equality to
console.log("x!=y:",x!=y); //Loose inequality
console.log("x!==y:",x!==y); //strict Not equal
console.log("x>3:",x>3); //Greater than
console.log("x<:",x<3); //Lesser than
console.log("x>=y:",x>=3); //Greater than or equal to
console.log("x<=y:",x<=3);//Less than or equal to 





